package Operators;

public class Relational_Operators {
    public static void main(String[] args) {
        // <,>,<=,>=,!=,==   these are the relational operators in java
        
        int a = 19;
        int b = 393;
        System.out.println(a==b);
        System.out.println(a<b);
        System.out.println(a>b);
        System.out.println(a<=b);
        System.out.println(a>=b);
        System.out.println(a!=b);
    }
}
