/*
Java is a high-level, object-oriented programming language
developed by James Gosling at Sun Microsystems.

Java follows the principle:
"Write Once, Run Anywhere (WORA)"

How Java works:

1) A source file is created with the extension .java
2) The Java compiler (javac) compiles the source code into bytecode,
   generating a .class file
3) The JVM executes the bytecode using:
   - An Interpreter (executes bytecode instruction by instruction)
   - A JIT (Just-In-Time) Compiler, which converts frequently executed
     bytecode into native machine code for better performance

Java Environment:

1) JDK (Java Development Kit)
   = JRE + Compiler + Debugger + Development tools

2) JRE (Java Runtime Environment)
   = JVM + Core libraries

Platform Independence:

- Bytecode is platform-independent
- JVM is platform-dependent (different JVMs for different operating systems)
*/
