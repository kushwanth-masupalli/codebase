package OOPs.Polymorphism;

/*
 * ===============================================
 *               Polymorphism in Java
 * ===============================================
 *
 * ✅ Definition:
 * Polymorphism means "many forms". It allows objects to take on multiple forms.
 * A single method name or operator behaves differently based on the context.
 *
 * ✅ Benefits:
 * - Increases flexibility and reusability
 * - Supports dynamic behavior at runtime
 * - Helps in code maintainability and scalability
 *
 * ✅ Types of Polymorphism in Java:
 * 1. Compile-time Polymorphism (Method Overloading)
 * 2. Runtime Polymorphism (Method Overriding)
 */
